    private void btnAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddItemActionPerformed
        // declare prices
        float prices[] = {5.00f, 4.00f, 6.00f, 3.00f};
        //local variables/read input
        String name = txtName.getText();
        int qty = Integer.parseInt(txtQty.getText());
        int orderNo = Integer.parseInt(txtOrder.getText());
        int index = cboType.getSelectedIndex();
        String burritoType = cboType.getSelectedItem().toString();
        float price = prices[index];
        
        Burrito memeBurrito = new Burrito(orderNo, name, burritoType, qty, price);
        
        lstOrder.add(memeBurrito.toString());
    }//GEN-LAST:event_btnAddItemActionPerformed

