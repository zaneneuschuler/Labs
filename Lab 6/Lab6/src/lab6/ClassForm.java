    private void btnNewAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewAccountActionPerformed
        // TODO add your handling code here:
        //declare variables, read text boses, etc
        String accountNo = txtAccount.getText();
        String name = txtName.getText();
        String address = txtAddress.getText();
        float balance = Float.parseFloat(txtBalance.getText() );
        
        //set account properties
        myAccount.setAccountNo(accountNo);
        myAccount.setName(name);
        myAccount.setAddress(address);
        myAccount.setBalance(balance);
        
        //display current account info
        listAccount.add ( myAccount.toString());
        
    }//GEN-LAST:event_btnNewAccountActionPerformed

